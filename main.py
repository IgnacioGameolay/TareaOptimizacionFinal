        historiales_convergencia.append(historial_convergencia)

        if mejor_costo_global is None or costo < mejor_costo_global:
          mejor_costo_global = costo
          mejor_solucion_global = mejor_solucion.copy()
          mejores_subconjuntos_global = subconjuntos_elegidos.copy()
          semilla_mejor_global = semilla_actual

