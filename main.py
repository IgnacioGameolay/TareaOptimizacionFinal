# Función para calcular la mediana
def calcular_mediana(valores):
    valores_ordenados = sorted(valores)
    n = len(valores_ordenados)
    mitad = n // 2

    if n % 2 == 1:
        return valores_ordenados[mitad]

    return (valores_ordenados[mitad - 1] + valores_ordenados[mitad]) / 2


# Función para calcular datos útiles para boxplot
def calcular_datos_boxplot(valores):
    valores_ordenados = sorted(valores)
    n = len(valores_ordenados)

    if n % 2 == 0:
        mitad_inferior = valores_ordenados[:n // 2]
        mitad_superior = valores_ordenados[n // 2:]
    else:
        mitad_inferior = valores_ordenados[:n // 2]
        mitad_superior = valores_ordenados[n // 2 + 1:]

    minimo = min(valores_ordenados)
    q1 = calcular_mediana(mitad_inferior)
    mediana = calcular_mediana(valores_ordenados)
    q3 = calcular_mediana(mitad_superior)
    maximo = max(valores_ordenados)

    return minimo, q1, mediana, q3, maximo


# Función para guardar datos de boxplot en CSV
def guardar_boxplot_csv(resumenes, ruta_salida):
    encabezados = ["instancia", "semilla", "minimo", "q1", "mediana", "q3", "maximo"]

