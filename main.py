# Función para calcular la convergencia promedio de una instancia
def calcular_convergencia_promedio(historiales):
    largo_minimo = len(historiales[0])
    promedio = []

    for historial in historiales:
        if len(historial) < largo_minimo:
            largo_minimo = len(historial)

    for i in range(largo_minimo):
        suma = 0

        for historial in historiales:
            suma += historial[i]

        promedio.append(suma / len(historiales))

    return promedio


