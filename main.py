# Función para generar un análisis textual breve de los resultados

def guardar_analisis_txt(resumenes, ruta_salida):
    """
    Genera un análisis textual con:
      - Métricas individuales por instancia (estabilidad, calidad)
      - Comparación numérica ENTRE instancias (diferencias de costo y tiempo)
    """
    with open(ruta_salida, "w", encoding="utf-8") as f:

        f.write("Análisis de resultados - Simulated Annealing para SCP\n")
        f.write("=====================================================\n\n")

        # ---- Sección 1: detalle por instancia ---------------------------
        f.write("1. DETALLE POR INSTANCIA\n")
        f.write("------------------------\n\n")

        for resumen in resumenes:
            f.write(f"Instancia: {resumen['instancia']}\n")
            f.write(f"  Valores obtenidos   : {resumen['resultados']}\n")
            f.write(f"  Mejor costo         : {resumen['mejor_costo_global']}\n")
            f.write(f"  Peor costo          : {resumen['peor_costo']}\n")
            f.write(f"  Media               : {round(resumen['media'], 4)}\n")
            f.write(f"  Desviación estándar : {round(resumen['desviacion_estandar'], 4)}\n")
            f.write(f"  Tiempo promedio     : {round(resumen['tiempo_promedio'], 6)} s\n")

            # Estabilidad
            if resumen["desviacion_estandar"] == 0:
                estabilidad = "completamente estable (todos los resultados fueron idénticos)."
            elif resumen["desviacion_estandar"] <= resumen["media"] * 0.05:
                estabilidad = "muy estable (desv. est. <= 5% de la media)."
            elif resumen["desviacion_estandar"] <= resumen["media"] * 0.10:
                estabilidad = "moderadamente estable (desv. est. entre 5% y 10% de la media)."
            else:
                estabilidad = (
                    f"poco estable (desv. est. = "
                    f"{round(resumen['desviacion_estandar'] / resumen['media'] * 100, 1)}% de la media). "
                    "Considerar aumentar iteraciones o ajustar temperatura."
                )

            f.write(f"  Estabilidad         : {estabilidad}\n")

            # Calidad (basada en dispersión mejor/media)
            diferencia_porcentual = (
                (resumen["media"] - resumen["mejor_costo_global"])
                / resumen["mejor_costo_global"] * 100
                if resumen["mejor_costo_global"] > 0 else 0
            )
            if diferencia_porcentual <= 5:
                calidad = "alta: la media se aleja menos del 5% del mejor costo."
            elif diferencia_porcentual <= 15:
                calidad = f"media: la media supera al mejor costo en un {round(diferencia_porcentual, 1)}%."
            else:
                calidad = (
                    f"baja: la media supera al mejor costo en un {round(diferencia_porcentual, 1)}%. "
                    "El algoritmo es inconsistente entre ejecuciones."
                )

            f.write(f"  Calidad de solución : {calidad}\n\n")

        # ---- Sección 2: comparación entre instancias --------------------
        if len(resumenes) > 1:
            f.write("2. COMPARACIÓN ENTRE INSTANCIAS\n")
            f.write("--------------------------------\n\n")

            # Tabla compacta
            f.write(
                f"{'Instancia':<12} {'Mejor':>10} {'Peor':>10} "
                f"{'Media':>10} {'Desv.Est.':>10} {'T.Prom.(s)':>12}\n"
            )
            f.write("-" * 60 + "\n")

            for resumen in resumenes:
                f.write(
                    f"{resumen['instancia']:<12} "
                    f"{resumen['mejor_costo_global']:>10} "
                    f"{resumen['peor_costo']:>10} "
                    f"{round(resumen['media'], 2):>10} "
                    f"{round(resumen['desviacion_estandar'], 2):>10} "
                    f"{round(resumen['tiempo_promedio'], 4):>12}\n"
                )

            f.write("\n")

            # Diferencias numéricas respecto a la instancia de referencia (primera)
            ref = resumenes[0]
            f.write(f"Referencia base: instancia '{ref['instancia']}'\n\n")

            for resumen in resumenes[1:]:
                diff_mejor = resumen["mejor_costo_global"] - ref["mejor_costo_global"]
                diff_media = resumen["media"] - ref["media"]
                diff_tiempo = resumen["tiempo_promedio"] - ref["tiempo_promedio"]

                pct_mejor = (
                    diff_mejor / ref["mejor_costo_global"] * 100
                    if ref["mejor_costo_global"] > 0 else 0
                )
                pct_media = (
                    diff_media / ref["media"] * 100
                    if ref["media"] > 0 else 0
                )

                f.write(f"Instancia '{resumen['instancia']}' vs '{ref['instancia']}':\n")
                f.write(
                    f"  Mejor costo  : {'+' if diff_mejor >= 0 else ''}{diff_mejor} "
                    f"({'+' if pct_mejor >= 0 else ''}{round(pct_mejor, 1)}%)\n"
                )
                f.write(
                    f"  Media        : {'+' if diff_media >= 0 else ''}{round(diff_media, 2)} "
                    f"({'+' if pct_media >= 0 else ''}{round(pct_media, 1)}%)\n"
                )
                f.write(
                    f"  Tiempo prom. : {'+' if diff_tiempo >= 0 else ''}{round(diff_tiempo, 4)} s\n\n"
                )

            # Instancia con mejor y peor desempeño global
            mejor_inst = min(resumenes, key=lambda r: r["mejor_costo_global"])
            peor_inst  = max(resumenes, key=lambda r: r["mejor_costo_global"])
            mas_estable = min(resumenes, key=lambda r: r["desviacion_estandar"])

            f.write("Conclusiones comparativas:\n")
            f.write(
                f"  - Mejor costo global     : instancia '{mejor_inst['instancia']}' "
                f"(costo = {mejor_inst['mejor_costo_global']})\n"
            )
            f.write(
                f"  - Peor costo global      : instancia '{peor_inst['instancia']}' "
                f"(costo = {peor_inst['mejor_costo_global']})\n"
            )
            f.write(
                f"  - Más estable            : instancia '{mas_estable['instancia']}' "
                f"(desv. est. = {round(mas_estable['desviacion_estandar'], 4)})\n"
            )

            # Tendencia de escalabilidad
            costos_medios = [r["media"] for r in resumenes]
            tiempos_medios = [r["tiempo_promedio"] for r in resumenes]

            if all(costos_medios[i] <= costos_medios[i + 1] for i in range(len(costos_medios) - 1)):
                tendencia_costo = "crece monotónicamente con el tamaño de la instancia."
            elif all(costos_medios[i] >= costos_medios[i + 1] for i in range(len(costos_medios) - 1)):
                tendencia_costo = "decrece monotónicamente (revisar la dificultad relativa de las instancias)."
            else:
                tendencia_costo = "no sigue una tendencia clara entre instancias."

            if all(tiempos_medios[i] <= tiempos_medios[i + 1] for i in range(len(tiempos_medios) - 1)):
                tendencia_tiempo = "el tiempo de ejecución escala correctamente con la dificultad."
            else:
                tendencia_tiempo = "el tiempo de ejecución no escala de forma monótona entre instancias."

            f.write(f"  - Tendencia de costo     : {tendencia_costo}\n")
            f.write(f"  - Tendencia de tiempo    : {tendencia_tiempo}\n")



            f.write("\n")

